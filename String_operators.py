str1 = "Hello"
str2 = "World"

# 1. Concatenation [+]
print("Concatenation:", str1 + " " + str2)

# 2. Repetition [*]
print("Repetition:", str1 * 3)

# 3. Slice []
print("Slice:", str1[1])   

# 4. Range Slice [:]
print("Range Slice:", str1[1:4])  

# 5. In Membership [in]
print("In Membership:", "H" in str1)

# 6. Not in Membership [not in]
print("Not in Membership:", "z" not in str1)