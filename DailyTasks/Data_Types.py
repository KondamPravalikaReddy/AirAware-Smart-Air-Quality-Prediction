# Integer (positive, negative, binary, octal, hexadecimal)
pos_int = 25
neg_int = -15
binary_int = 0b1011      
octal_int = 0o17         
hex_int = 0x1A           
print("Positive Integer:", pos_int)
print("Negative Integer:", neg_int)
print("Binary Integer:", binary_int)
print("Octal Integer:", octal_int)
print("Hexadecimal Integer:", hex_int)

# Float
float_num = 12.75
print("Float:", float_num)

# Complex
complex_num = 3 + 4j
print("Complex Number:", complex_num)

# Boolean
is_true = True
is_false = False
print("Boolean True:", is_true)
print("Boolean False:", is_false)
