# Tuple
my_tuple = (10, 20, 30, 40)
print("Tuple elements:")
for item in my_tuple:
    print(item)

# Dictionary
my_dict = {"name": "Alice", "age": 22, "city": "Hyderabad"}
print("\nDictionary elements:")
for key, value in my_dict.items():
    print(key, ":", value)

# Set
my_set = {100, 200, 300, 400}
print("\nSet elements:")
for item in my_set:
    print(item)